const ENV_FILE = path.join(ROOT, 'backend', '.env');

let setupLog  = [];
let setupDone = false;
let setupErr  = null;
let setupRunning = false;

function gen(bytes = 48) { return crypto.randomBytes(bytes).toString('hex'); }

function openBrowser(url) {
  const cmd = process.platform === 'win32' ? `start ${url}`
    : process.platform === 'darwin' ? `open ${url}` : `xdg-open ${url} 2>/dev/null || true`;
  try { execSync(cmd); } catch {}
}

function log(msg) {
  setupLog.push({ t: new Date().toISOString(), msg });
  process.stdout.write(msg + '\n');
}

function runCmd(cmd, opts = {}) {
  return new Promise((resolve, reject) => {
    log(`$ ${cmd}`);
    const child = spawn('sh', ['-c', cmd], { cwd: ROOT, env: { ...process.env, FORCE_COLOR: '0' }, ...opts });
    child.stdout?.on('data', d => log(d.toString().trim()));
    child.stderr?.on('data', d => log(d.toString().trim()));
    child.on('close', code => code === 0 ? resolve() : reject(new Error(`Command failed (exit ${code}): ${cmd}`)));
  });
}

function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

async function runSetup(cfg) {
  setupRunning = true;
  setupLog = [];
  setupDone = false;
  setupErr = null;

  try {
    // ── Step 1: Write .env ──────────────────────────────────────
    log('📝 Writing backend/.env ...');
    const dbPass = cfg.dbPassword || 'anymentor123';
    const dbName = cfg.dbName    || 'anymentor';
    const dbUser = cfg.dbUser    || 'anymentor';

    const env = `# Generated by AnyMentor Setup Wizard
NODE_ENV=development
PORT=3000

# Database
DATABASE_URL=postgresql://${dbUser}:${dbPass}@postgres:5432/${dbName}
POSTGRES_USER=${dbUser}
POSTGRES_PASSWORD=${dbPass}
POSTGRES_DB=${dbName}

# JWT
JWT_ACCESS_SECRET=${gen()}
JWT_REFRESH_SECRET=${gen()}
JWT_ACCESS_EXPIRES=15m
JWT_REFRESH_EXPIRES=7d

# Owner Account
OWNER_EMAIL=${cfg.ownerEmail}
OWNER_PASSWORD=${cfg.ownerPassword}

# First Company
COMPANY_NAME=${cfg.companyName}
COMPANY_SLUG=${cfg.companySlug}
ADMIN_EMAIL=${cfg.adminEmail}
ADMIN_PASSWORD=${cfg.adminPassword}

# Razorpay (Indian Payments)
RAZORPAY_KEY_ID=${cfg.razorpayKeyId || ''}
RAZORPAY_KEY_SECRET=${cfg.razorpaySecret || ''}
RAZORPAY_WEBHOOK_SECRET=${cfg.razorpayWebhook || ''}

# PhonePe
PHONEPE_MERCHANT_ID=${cfg.phonepeId || ''}
PHONEPE_SALT_KEY=${cfg.phonepeSalt || ''}
PHONEPE_SALT_INDEX=${cfg.phonepeSaltIndex || '1'}
PHONEPE_ENV=${cfg.phonepeEnv || 'SANDBOX'}

# Stripe (International)
STRIPE_SECRET_KEY=${cfg.stripeSecret || ''}
STRIPE_WEBHOOK_SECRET=${cfg.stripeWebhook || ''}
STRIPE_PUBLISHABLE_KEY=${cfg.stripePublic || ''}

# UPI
UPI_VPA=${cfg.upiVpa || ''}
UPI_NAME=${cfg.companyName || 'AnyMentor'}

# Email (SMTP)
SMTP_HOST=${cfg.smtpHost || 'smtp.gmail.com'}
SMTP_PORT=${cfg.smtpPort || '587'}
SMTP_USER=${cfg.smtpUser || ''}
SMTP_PASS=${cfg.smtpPass || ''}
SMTP_FROM=${cfg.smtpFrom || cfg.ownerEmail}

# File Upload
UPLOAD_DIR=uploads
MAX_FILE_SIZE_MB=10

# Redis
REDIS_URL=redis://redis:6379

# Security
BCRYPT_ROUNDS=12
RATE_LIMIT_WINDOW_MS=900000
RATE_LIMIT_MAX=100

# Frontend
FRONTEND_URL=http://localhost:5173
ALLOWED_ORIGINS=http://localhost:5173,http://localhost:3000
`;
    fs.writeFileSync(ENV_FILE, env, 'utf-8');
    log('✅ .env written');

    // Also update docker-compose postgres env vars
    log('📦 Starting Docker containers ...');
    await runCmd('docker-compose up -d --build');

    log('⏳ Waiting for PostgreSQL to be ready ...');
    for (let i = 0; i < 30; i++) {
      try {
        execSync('docker-compose exec -T postgres pg_isready -U anymentor', { stdio: 'ignore', cwd: ROOT });
        log('✅ PostgreSQL is ready');
        break;
      } catch {
        await sleep(2000);
        log(`   Still waiting... (${i + 1}/30)`);
      }
    }

    log('🗃️  Running database migrations ...');
    await runCmd('docker-compose exec -T backend npx prisma migrate deploy');

    log('🌱 Seeding database ...');
    await runCmd('docker-compose exec -T backend node prisma/seed.js');

    log('');
    log('🎉 Setup complete!');
    log(`   Frontend  → http://localhost:5173`);
    log(`   API Docs  → http://localhost:3000/api/docs`);
    log(`   Owner     → ${cfg.ownerEmail}`);
    log(`   Admin     → ${cfg.adminEmail}`);

    setupDone = true;
  } catch (err) {
    setupErr = err.message;
    log('❌ Setup failed: ' + err.message);
  } finally {
    setupRunning = false;
  }
}

