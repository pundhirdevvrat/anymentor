// Auto-generate slug from company name
document.getElementById('companyNameInput').addEventListener('input', function() {
  const slug = this.value.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
  document.getElementById('companySlugInput').value = slug;
});

let pollInterval;
let startTime;
let savedCfg = {};

document.getElementById('form').addEventListener('submit', async function(e) {
  e.preventDefault();
  const fd = new FormData(this);
  const cfg = Object.fromEntries(fd.entries());
  savedCfg = cfg;

  document.getElementById('form').style.display = 'none';
  document.getElementById('progress').classList.add('show');
  document.getElementById('submitBtn').disabled = true;
  startTime = Date.now();

  // Send setup request
  fetch('/setup', {
    method: 'POST',
    headers: {'Content-Type': 'application/json'},
    body: JSON.stringify(cfg)
  }).catch(() => {});

  // Poll for status
  pollInterval = setInterval(pollStatus, 1500);
  pollStatus();
});

let lastLogCount = 0;
async function pollStatus() {
  try {
    const res = await fetch('/status');
    const data = await res.json();

    const logBox = document.getElementById('logBox');
    const newLogs = data.logs.slice(lastLogCount);
    newLogs.forEach(entry => {
      const div = document.createElement('div');
      div.textContent = '> ' + entry.msg;
      if (entry.msg.includes('❌') || entry.msg.includes('Error') || entry.msg.includes('failed')) {
        div.className = 'err';
      }
      logBox.appendChild(div);
    });
    lastLogCount = data.logs.length;
    logBox.scrollTop = logBox.scrollHeight;

    // Update progress bar
    const pct = Math.min(95, (Date.now() - startTime) / 1200);
    document.getElementById('progressBar').style.width = pct + '%';

    if (data.done) {
      clearInterval(pollInterval);
      document.getElementById('progressBar').style.width = '100%';
      document.getElementById('progressTitle').textContent = '✅ Setup Complete!';
      showSuccess();
    } else if (data.error) {
      clearInterval(pollInterval);
      document.getElementById('progressTitle').textContent = '❌ Setup Failed';
      document.getElementById('progressBar').style.background = '#ff5252';
    }
  } catch {}
}

